# Simple ASCII Art
def print_ascii_art():
    art = r"""
             (     )
          ( ) |   | ( ) [ ]
         /  /       \  \  |   
        |  /         \  | \  
        \__|         |__/ \
    ____@__\_________/__@____\__
    """

    print(art)

if __name__ == "__main__":
    print_ascii_art()
